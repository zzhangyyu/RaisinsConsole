package com.yoler.raisinsconsole.vo;

public class PatientBriefInfoVo {
    private String patientInfoId;
    private String patientName;
    private String patientSex;
    private String patientAge;
    private String lastVisitingDate;
    private String consiliaCnt;

    public String getPatientInfoId() {
        return patientInfoId;
    }

    public void setPatientInfoId(String patientInfoId) {
        this.patientInfoId = patientInfoId;
    }

    public String getPatientName() {
        return patientName;
    }

    public void setPatientName(String patientName) {
        this.patientName = patientName;
    }

    public String getPatientSex() {
        return patientSex;
    }

    public void setPatientSex(String patientSex) {
        this.patientSex = patientSex;
    }

    public String getPatientAge() {
        return patientAge;
    }

    public void setPatientAge(String patientAge) {
        this.patientAge = patientAge;
    }

    public String getLastVisitingDate() {
        return lastVisitingDate;
    }

    public void setLastVisitingDate(String lastVisitingDate) {
        this.lastVisitingDate = lastVisitingDate;
    }

    public String getConsiliaCnt() {
        return consiliaCnt;
    }

    public void setConsiliaCnt(String consiliaCnt) {
        this.consiliaCnt = consiliaCnt;
    }
}
